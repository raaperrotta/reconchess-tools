def test_sense_result_to_string():
    result = [
        (11, chess.Piece.from_symbol("P")),
        (12, None),
        (13, chess.Piece.from_symbol("P")),
        (19, None),
        (20, None),
        (21, None),
        (27, None),
        (28, chess.Piece.from_symbol("P")),
        (29, None),
    ]
    assert sense_result_to_string(result) == "P_P____P_"


def test_sense_result_from_string():
    target_square = 20
    result = [
        (11, chess.Piece.from_symbol("P")),
        (12, None),
        (13, chess.Piece.from_symbol("P")),
        (19, None),
        (20, None),
        (21, None),
        (27, None),
        (28, chess.Piece.from_symbol("P")),
        (29, None),
    ]
    assert sense_result_from_string(target_square, "P_P____P_") == result


def test_sense_result_string_round_trip():
    string = "QqKkNnRr_"
